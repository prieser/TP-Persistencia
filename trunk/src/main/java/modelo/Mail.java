package modelo;

public class Mail {
	private	 int body;
	private	 String subject;
	private	 String to;
	private	 String from;
	
	
	
	public Mail(int body, String subject, String to, String from) {
		super();
		this.body = body;
		this.subject = subject;
		this.to = to;
		this.from = from;
	}
	
	
}
